def generate_meditation(med_id: str, med_data: dict):
    """Generate TTS audio for a meditation and save as MP3."""
    output_path = os.path.join(OUTPUT_DIR, f"meditation_{med_id}.mp3")

    if os.path.exists(output_path):
        size = os.path.getsize(output_path)
        if size > 10000:  # Skip if already generated (>10KB)
            print(f"  [skip] {med_data['title']} — already exists ({size//1024} KB)")
            return True

    text = med_data["text"].strip()
    print(f"  Generating {med_data['title']} ({len(text)} chars)...")

    try:
        r = requests.post(
            f"https://api.elevenlabs.io/v1/text-to-speech/{VOICE_ID}",
            headers={
                "xi-api-key": API_KEY,
                "Content-Type": "application/json",
            },
            json={
                "text": text,
                "model_id": MODEL_ID,
                "voice_settings": VOICE_SETTINGS,
            },
            timeout=120,
        )

        if r.status_code == 200:
            with open(output_path, "wb") as f:
                f.write(r.content)
            print(f"  [ok] {med_data['title']} — {len(r.content)//1024} KB")
            return True
        else:
            print(f"  [err] {med_data['title']} — {r.status_code}: {r.text[:200]}")
            return False
    except Exception as e:
        print(f"  [err] {med_data['title']} — {e}")
        return False


def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    print(f"Generating {len(MEDITATIONS)} meditation audio files...\n")

    success = 0
    for med_id in sorted(MEDITATIONS.keys()):
        ok = generate_meditation(med_id, MEDITATIONS[med_id])
        if ok:
            success += 1
        time.sleep(2)  # Rate limit courtesy

    print(f"\nDone: {success}/{len(MEDITATIONS)} generated successfully.")
    print(f"Output: {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
