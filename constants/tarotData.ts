export function getCardById(id: number): TarotCard | undefined {
  return TAROT_CARDS.find((c) => c.id === id);
}

export function drawRandomCards(count: number): TarotCard[] {
  const shuffled = [...TAROT_CARDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getYesNoAnswer(card: TarotCard): { answer: string; color: string } {
  switch (card.yesNo) {
    case 'yes':   return { answer: 'ТАК',    color: '#10B981' };
    case 'no':    return { answer: 'НІ',     color: '#EF4444' };
    case 'maybe': return { answer: 'МОЖЛИВО', color: '#F59E0B' };
  }
}
